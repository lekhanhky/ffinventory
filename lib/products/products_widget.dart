          elevation: 2.0,
        ),
        body: SafeArea(
          top: true,
          child: Column(
            mainAxisSize: MainAxisSize.max,
            children: [
              RefreshIndicator(
                onRefresh: () async {
                  safeSetState(
                      () => _model.listViewPagingController?.refresh());
                  await _model.waitForOnePageForListView();
